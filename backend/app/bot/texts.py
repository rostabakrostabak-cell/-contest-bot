"""Все тексты UI на одном месте."""
from dataclasses import dataclass

T = str


@dataclass
class Texts:
    # ─── Главное меню ─────────────────────────────────────────────────────────
    main_menu: T = (
        "<b>Добро пожаловать!</b>\n\n"
        "Выберите действие:"
    )

    # ─── Отправка чека ─────────────────────────────────────────────────────────
    pick_shop: T = "Выберите <b>магазин</b>:"
    enter_name: T = (
        "Введите <b>Фамилию и Имя</b>.\n\n"
        "Формат: <code>Фамилия Имя</code>\n"
        "Пример: <code>Иванов Пётр</code>"
    )
    name_error: T = (
        "Неверный формат. Нужно ровно два слова (Фамилия и Имя), "
        "каждое минимум 2 буквы.\n\n"
        "Попробуйте ещё раз:"
    )
    pick_category: T = "Выберите <b>категорию</b>:"
    enter_amount: T = (
        "Введите <b>сумму чека</b>.\n\n"
        "Примеры: <code>4500</code>, <code>4 500,50</code>"
    )
    amount_error: T = (
        "Не удалось распознать сумму. Введите число больше 0.\n"
        "Примеры: <code>4500</code>, <code>4 500,50</code>"
    )
    attach_photo: T = (
        "Пришлите <b>фото чека</b>.\n\n"
        "📷 Одно фото на одну заявку."
    )
    not_a_photo: T = "Это не фотография. Пожалуйста, пришлите <b>фото</b> чека."
    submit_success: T = (
        "✅ <b>Заявка отправлена!</b>\n\n"
        "Номер: <code>#{receipt_id}</code>\n"
        "Ожидайте решения администратора."
    )
    submissions_closed: T = (
        "⛔ <b>Приём чеков завершён.</b>\n\n"
        "Ознакомьтесь с результатами в разделе «🧪 Колба»."
    )

    # ─── Админ — модерация ───────────────────────────────────────────────────
    approved_notification: T = (
        "✅ <b>Чек подтверждён!</b>\n\n"
        "Заявка <code>#{receipt_id}</code> одобрена."
    )
    rejected_notification: T = (
        "❌ <b>Чек отклонён.</b>\n\n"
        "Заявка <code>#{receipt_id}</code>.\n"
        "Причина: {reason}"
    )
    retry_notification: T = (
        "↩️ <b>Чек на доработку.</b>\n\n"
        "Заявка <code>#{receipt_id}</code>.\n"
        "{reason}"
    )
    admin_menu: T = "<b>⚙️ Админ-панель</b>\n\nВыберите действие:"

    # ─── Разное ───────────────────────────────────────────────────────────────
    cancel: T = "Действие отменено."
    unknown: T = "Не понял. Воспользуйтесь меню."


def fmt_amount(amount) -> str:
    return f"{float(amount):,.2f}".replace(",", " ")
